int LinuxParser::ParseProcesses(string key) {
  string line;
  string tmp;
  std::ifstream filestream(kProcDirectory + kStatFilename);
  if (filestream.is_open()) {
    while (std::getline(filestream, line)) {
      std::istringstream linestream(line);

      linestream >> tmp;
      if (tmp == key) {
        linestream >> tmp;
        return stoi(tmp);
      }
    }
  }

  return 0;
}

